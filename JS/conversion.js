// >>>>>>>>>>>>>>>>>>>>>>   TYPE CONVERSION <<<<<<<<<<<<<<<<<<<<<<<<<<<<

let number = 22;
console.log(number);
console.log(typeof number);

let stringNum = "22";
console.log(typeof stringNum);
console.log(stringNum);


// converting Data type string into Number

let stringNumber = Number(stringNum);
console.log(typeof stringNumber);
console.log(stringNumber);
